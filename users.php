                $align = 'L'; if (strpos($originalValue, 'â‚±') !== false) { $align = 'R'; } elseif (in_array($header[$i], ['Status', 'Role'])) { $align = 'C'; }
                $this->Cell($widths[$i], 6, $cellValue, 'LR', 0, $align, true);
